"""
Elabore um algoritmo que receba a temperatura em graus Celsius e apresente o valor convertido
para graus Fahrenheit.
"""

temperaturaCelsius = float(input("Digite a temperatura em graus Celsius: "))
temperaturaFahrenheit = (temperaturaCelsius * 1.8) + 32

print("A conversão de Celsius para Fahrenheit é igual a: ", temperaturaFahrenheit)

